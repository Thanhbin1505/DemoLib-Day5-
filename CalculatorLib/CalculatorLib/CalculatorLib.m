#import "CalculatorLib.h"

@implementation CalculatorLib
-(int) addition:(int) a secondParam: (int) b
{
    return a+b;
}
-(int) subtraction:(int) a secondParam:(int) b
{
    return a - b;
}
-(int) multiply:(int) a secondParam: (int) b
{
    return a*b;
}
-(int) divison:(int) a secondParam: (int) b
{
    return a/b;
}
@end
